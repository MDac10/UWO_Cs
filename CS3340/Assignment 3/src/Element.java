public class Element {
	
	private int key;
	private int id;
	
	public Element(int key, int id) {
		this.key = key;
		this.id = id;
	}
	
	public int getKey() {
		return key;
	}
	
	public int getId() {
		return id;
	}
	
	public void setKey(int k) {
		key = k;
	}
	
	public void setId(int i) {
		id = i;
	}

}
